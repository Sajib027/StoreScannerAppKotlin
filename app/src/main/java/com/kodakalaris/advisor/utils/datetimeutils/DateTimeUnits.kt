package com.kodakalaris.advisor.utils.datetimeutils

/**
 * DateTimeUnits
 * Define units used by [DateTimeUtils.getDateDiff]
 * and also [DateTimeUtils.formatDate]
 */
enum class DateTimeUnits {
    /**
     * Days
     */
    DAYS,

    /**
     * Hours
     */
    HOURS,

    /**
     * Minutes
     */
    MINUTES,

    /**
     * Seconds
     */
    SECONDS,

    /**
     * Milliseconds
     */
    MILLISECONDS
}
